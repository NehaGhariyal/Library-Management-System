    public void faulterdetails_table(){
      
        LocalDate todaysdate= java.time.LocalDate.now();
        java.sql.Date sqlDate = java.sql.Date.valueOf( todaysdate);
        
        try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3306/library_mgnt","root","");
           PreparedStatement pst=con.prepareStatement("Select * from issue_details where due_date<? and status =?");
           pst.setDate(1, sqlDate);
           pst.setString(2,"Pending");
           ResultSet rs= pst.executeQuery();
           
           while (rs.next()){
                int id =  rs.getInt("id");
               String bookName=rs.getString("book_name");
               String studentName=rs.getString("student_name");
               String issueDate=rs.getString("issue_date");
               String dueDate=rs.getString("due_date");
               String status=rs.getString("status");
               
               Object[] obj={id,bookName,studentName,issueDate,dueDate,status};
               model=(DefaultTableModel)vi_bd.getModel();
               model.addRow(obj);
           }
             
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
